#ifndef MAPPING_TABLES_H
#define MAPPING_TABLES_H

/* Prevent typedef redefinition errors when integrating with libimli */
#ifndef __IMLI_H__
typedef unsigned char byte_t;
#endif

/**
 * ISCII-91 DIRECT MAPPING DATA
 * ----------------------------
 * This file provides the lookup arrays for Indic scripts.
 * * ASCII LOGIC:
 * No table is needed for ASCII (0-127). These characters are 
 * mapped 1:1. A space (0x20) in UTF-8 becomes 0x20 in ISCII.
 * * INDIC LOGIC:
 * Unicode offsets are used to index the tables below to retrieve
 * the corresponding 8-bit ISCII-91 byte.
 */

// --- FORWARD MAPPING TABLES (Relative Offset -> ISCII BYTE) ---

/**
 * Consonant Mapping Table
 * Maps Unicode offsets 0x15 to 0x39.
 * Also includes Nukhta at the final index for offset 0x3C.
 */
static const byte_t imli_to_iscii_con[] = {
    0,    0xB3, 0xB4, 0xB5, 0xB6, 0xB7, 0xB8, 0xB9, 0xBA, 0xBB, 
    0xBC, 0xBD, 0xBE, 0xBF, 0xC0, 0xC1, 0xC2, 0xC3, 0xC4, 0xC5, 
    0xC6, 0x20, 0xC7, 0xC8, 0xC9, 0xCA, 0xCB, 0xCC, 0xCD, 0xCE, 
    0xCF, 0x20, 0x20, 0x20, 0xD0, 0xD1, 0xD2, 0xD3, 0xD4, 0xD5, 
    0xD6, 0xD7, 0xD8, 0x20, 0x20, 0x20, 0x20, 0x20, 0x20, 0x20, 
    0x20, 0x20, 0x20, 0x20, 0xE9  // Index 54 represents Nukhta
};

/**
 * Standalone Vowel Mapping Table
 * Maps Unicode offsets 0x05 to 0x14.
 */
static const byte_t imli_to_iscii_vow[] = {
    0xA4, 0xA5, 0xA6, 0xA7, 0xA8, 0xA9, 0xAA, 0xAB, 
    0xAC, 0xAD, 0xAE, 0xAF, 0xB0, 0xB1, 0xB2
};

/**
 * Dependent Vowel Matra Mapping Table
 * Maps Unicode offsets 0x3E to 0x4C.
 */
static const byte_t imli_to_iscii_vow_matras[] = {
    0x00, 0xDA, 0xDB, 0xDC, 0xDD, 0xDE, 0xDF, 0xE0, 
    0xE1, 0xE2, 0xE3, 0xE4, 0xE5, 0xE6, 0xE7
};

#endif // MAPPING_TABLES_H